if __name__ == "__main__":
    import argparse

    # Add repo root to path so assert_remote import works when run as script.
    if str(REPO) not in sys.path:
        sys.path.insert(0, str(REPO))
    try:
        from scripts.agent.assert_remote import assert_remote  # type: ignore

        assert_remote(allow_override=True)
    except ImportError:
        pass

    p = argparse.ArgumentParser(description="R2 push helper — sign-url subcommand")
    sub = p.add_subparsers(dest="cmd", required=True)

    sp = sub.add_parser("sign-url", help="generate a time-limited signed URL")
    sp.add_argument("--key", required=True, help="R2 object key")
    sp.add_argument("--ttl", type=int, default=3600, help="seconds (default 3600)")

    args = p.parse_args()

    if args.cmd == "sign-url":
        try:
            print(sign_url(key=args.key, ttl_seconds=args.ttl))
        except R2PushError as e:
            sys.stderr.write(f"❌ {e}\n")
            sys.exit(1)
